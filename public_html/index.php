      <p itemprop="articleBody">
      The amount is a matter of debate, but by any measure significant: between $1.2 billion, projected by Gov. Jerry Brown, and $4.4 billion, the estimate of the Legislature’s independent financial analyst. The surplus comes barely three years after the state was facing a deficit of close to $60 billion.</p>

      <p itemprop="articleBody">
      At first glance, the situation should be welcome news in a state overwhelmingly controlled by Democrats, who have spent much of their time slashing programs they support. After last November’s elections, the party has two-thirds majorities in the Assembly and the Senate, relegating Republicans almost completely to the sidelines.</p>

      <p itemprop="articleBody">
      Instead, the surplus has set off a debate about the durability of new revenues, and whether the money should be used to reverse some of the spending cuts or set aside to guard against the inevitable next economic downturn.</p>

      <p itemprop="articleBody">
      At least seven other states — among them Connecticut, Utah and Wisconsin — have reported budget surpluses in recent weeks, setting the stage for legislative battles that, if not as wrenching as the ones over cuts, promise to be no less pitched. Lawmakers are debating whether the new money should be used to restore programs cut during the recession, finance tax cuts or put into a rainy-day fund for future needs.</p>

      <p itemprop="articleBody">
      The debate reflects uncertainty about whether the revenue is a one-time event, a result of state taxes on wealthy residents selling off investments at the end of last year to avoid increased costs as the Bush-era federal tax cuts expired. But it also illustrates philosophical differences about the role of government, about spending versus taxes and about the need, as Mr. Brown argued, to learn lessons from a decade in which many states saw the bottom fall out from their revenue collections.</p>

      <p itemprop="articleBody">
      “We’re seeing a change in conversation in state legislatures this year,” said Todd Haggerty, a policy analyst with the <a href="http://www.ncsl.org/">National Conference of State Legislatures</a>. “They’re not talking about how to close a budget gap anymore, which is a welcome relief after years of that during and after the Great Recession. Rather, states are having conversations about how to allocate increased revenues.”</p>

      <p itemprop="articleBody">
      Nowhere does that battle promise to play out with more force and intricacy than in California, the state that underwent perhaps the most severe retrenchments in the country.</p>

      <p itemprop="articleBody">
      Mr. Brown, a Democrat who has always had a fiscally conservative streak, is leading the don’t-pop-any-Champagne-corks brigade, saying that he would oppose significant increases in new spending and that the money should go into a rainy-day fund. His administration put out the lower $1.2 billion estimate.</p>

      <p itemprop="articleBody">
      “A good deal of the surge of revenues that we have seen since the beginning of the year is the result of higher-income individuals being able to realize some of their gains at the end of 2012,” said H. D. Palmer, the director of external affairs for the California Department of Finance. “We don’t believe it is prudent to budget on the capital gains. It wasn’t that long ago when we had the same experience during the dot-com boom. We don’t want to see that movie again.”</p>

      <p itemprop="articleBody">
      In one particularly revealing moment, the office of the independent legislative analyst — which has a history of scolding governors for unrealistically optimistic budget projections — <a href="http://www.lao.ca.gov/reports/2013/bud/may-revise/overview-may-revise-051713.pdf">dismissed Mr. Brown’s figure</a> as pessimistic, saying extra revenue was closer to $4.4 billion. The report undercut Mr. Brown just as he and lawmakers moved into the final stages of budget negotiations, and it empowered Democrats and social service advocates eager to reverse budget cuts.</p>

      <p itemprop="articleBody">
      “I support the governor’s call to pay down more debt aggressively, I support the notion of a rainy-day fund,” said Darrell Steinberg, the president pro tem of the Senate. “But I also believe that we have an obligation to make some limited but important investments in restoring some of what has been lost over the last four or five years.”</p>

      <p itemprop="articleBody">
      Vanessa Aramayo, the director of <a href="http://www.california-partnership.org/blog/">California Partnership</a>, a group of organizations pushing for social service spending, said Mr. Brown was deliberately understating the state’s financial health.</p>

      <p itemprop="articleBody">
      “The governor is attempting to leave a legacy of solving our state budget crisis,” she said. “But he’s doing so on the backs of poor people in the state.”</p>

